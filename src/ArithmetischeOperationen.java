public class ArithmetischeOperationen {

    public static void main(String[] args) {

        int a = 232;
        int b = 67;

        int add = a + b;
        int sub = a - b;
        int div = a / b;
        int mul = a * b;
        int mod = a % b;

        System.out.println(add);
        System.out.println(sub);
        System.out.println(div);
        System.out.println(mul);
        System.out.println(mod);
    }
}
